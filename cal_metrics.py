import numpy as np

def calculate_stats(data):
    # 提取第三列（MSE）和第四列（MAE）
    mse_values = [float(row[2]) for row in data]
    mae_values = [float(row[3]) for row in data]
    
    # 计算平均值和标准差
    mse_mean = round(np.mean(mse_values), 3)
    mse_std = round(np.std(mse_values), 3)
    
    mae_mean = round(np.mean(mae_values), 3)
    mae_std = round(np.std(mae_values), 3)
    
    return {
        'mse_mean': mse_mean,
        'mse_std': mse_std,
        'mae_mean': mae_mean,
        'mae_std': mae_std
    }

